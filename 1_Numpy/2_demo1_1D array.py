# numpy ===> array
import numpy as np
a=np.array([1,2,3,4,5]) #creating array
print(a)
print(type(a)) # type of array
# <class 'numpy.ndarray'>


# Diamension ==> ndim  ==> for finding the dimention of the array
print(a.ndim)
# 1


# Order ==>shape
print(a.shape)
# (5,)

# to find the size of the array
print(a.size)
#5
